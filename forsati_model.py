def normalize_value(value, min_value, max_value):
    if max_value == min_value:
        return 0.5
    return max(0.0, min(1.0, (value - min_value) / (max_value - min_value)))


def show_suggestions(input_name, valid_names, title):
    suggestions = get_close_matches(input_name, valid_names, n=3, cutoff=0.4)
    print(f" {title} not found: '{input_name}'")
    if suggestions:
        print('Did you mean:')
        for s in suggestions:
            print(f'   - {s}')
    else:
        print('   No close match found.')


def build_candidate_row(df, category_name, neighborhood_name, feature_cols):
    valid_categories    = df['category_en'].dropna().unique()
    valid_neighborhoods = df['neighborhood'].dropna().unique()

    if category_name not in valid_categories:
        show_suggestions(category_name, valid_categories, 'Category')
        return None

    if neighborhood_name not in valid_neighborhoods:
        show_suggestions(neighborhood_name, valid_neighborhoods, 'Neighborhood')
        return None

    pair_rows         = df[(df['category_en'] == category_name) & (df['neighborhood'] == neighborhood_name)]
    neighborhood_rows = df[df['neighborhood'] == neighborhood_name]
    category_rows     = df[df['category_en'] == category_name]

    if len(pair_rows) > 0:
        base = pair_rows.mean(numeric_only=True)
    elif len(neighborhood_rows) > 0 and len(category_rows) > 0:
        # Blend neighborhood + category means for a better synthetic row
        base = (neighborhood_rows.mean(numeric_only=True) + category_rows.mean(numeric_only=True)) / 2
    elif len(neighborhood_rows) > 0:
        base = neighborhood_rows.mean(numeric_only=True)
    else:
        base = df.mean(numeric_only=True)

    row = pd.DataFrame([base])

    # Restore categorical encodings from actual data
    row['category_encoded']          = df[df['category_en'] == category_name]['category_encoded'].iloc[0]
    row['neighborhood_success_rate'] = neighborhood_success_rate.get(neighborhood_name, df['success'].mean())
    row['category_success_rate']     = category_success_rate.get(category_name, df['success'].mean())

    # Recompute transport_index to stay consistent
    if all(c in row.columns for c in ['metro', 'bus', 'public_station']):
        row['transport_index'] = row['metro'] + row['bus'] + row['public_station']

    for col in feature_cols:
        if col not in row.columns:
            row[col] = 0

    return row[feature_cols]


def recommendation_score(
    df,
    model,
    feature_cols,
    category_name,
    neighborhood_name,
    show_details=True
):
    row = build_candidate_row(df, category_name, neighborhood_name, feature_cols)
    if row is None:
        return None

    ml_probability = model.predict_proba(row)[0][1]
    values = row.iloc[0]

    demand_score = normalize_value(
        values['population_density'],
        df['population_density'].min(), df['population_density'].max()
    )

    nearby_activity_score = normalize_value(
        values['nearby_all_businesses'],
        df['nearby_all_businesses'].min(), df['nearby_all_businesses'].max()
    )

    transport_score = normalize_value(
        values['transport_index'],
        (df['metro'] + df['bus'] + df['public_station']).min(),
        (df['metro'] + df['bus'] + df['public_station']).max()
    )

    market_strength_score = normalize_value(
        values['business_licenses'],
        df['business_licenses'].min(), df['business_licenses'].max()
    )

    competition = values['competition_ratio']
    ideal_competition = df['competition_ratio'].median()
    if ideal_competition == 0:
        competition_score = 0.5
    else:
        competition_score = max(0.0, min(1.0,
            1 - abs(competition - ideal_competition) / ideal_competition
        ))

    final_score = (
        0.40 * ml_probability +
        0.20 * demand_score +
        0.15 * competition_score +
        0.10 * nearby_activity_score +
        0.10 * transport_score +
        0.05 * market_strength_score
    )
    final_score = round(final_score * 100, 2)

    if final_score >= 70:   label = 'مناسب جداً'
    elif final_score >= 50: label = 'مناسب'
    else:                   label = 'غير مناسب'

    if show_details:
        print('\n' + '=' * 55)
        print(f'التقييم الذكي لمشروع: {category_name.upper()}')
        print(f'الحي: {neighborhood_name}')
        print('=' * 55)
        print(f'النتيجة النهائية: {final_score}/100  {label}')
        print('-' * 55)
        print('تفاصيل التقييم:\n')
        print(f'    توقع النموذج:          {ml_probability * 100:6.1f}')
        print(f'    الطلب (الكثافة):       {demand_score * 100:6.1f}')
        print(f'     المنافسة:             {competition_score * 100:6.1f}')
        print(f'    النشاط القريب:         {nearby_activity_score * 100:6.1f}')
        print(f'    المواصلات:             {transport_score * 100:6.1f}')
        print(f'    قوة السوق:             {market_strength_score * 100:6.1f}')
        print('=' * 55)

    return {
        'category': category_name,
        'neighborhood': neighborhood_name,
        'final_score': final_score,
        'ml_probability': round(ml_probability * 100, 2),
        'demand_score': round(demand_score * 100, 2),
        'competition_score': round(competition_score * 100, 2),
        'nearby_activity_score': round(nearby_activity_score * 100, 2),
        'transport_score': round(transport_score * 100, 2),
        'market_strength_score': round(market_strength_score * 100, 2)
    }


def rank_neighborhoods(df, model, feature_cols, category_name, top_n=10):
    if category_name not in df['category_en'].dropna().unique():
        show_suggestions(category_name, df['category_en'].dropna().unique(), 'Category')
        return pd.DataFrame()

    neighborhoods = sorted(df['neighborhood'].dropna().unique())
    results = []

    for neighborhood in neighborhoods:
        score = recommendation_score(
            df, model, feature_cols, category_name, neighborhood, show_details=False
        )
        if score is not None:
            results.append(score)

    if not results:
        print('No results found.')
        return pd.DataFrame()

    results_df = pd.DataFrame(results).sort_values('final_score', ascending=False).reset_index(drop=True)

    print(f'\n=== أفضل {top_n} أحياء لـ {category_name} ===')
    print(f'{"#":<4} {"الحي":<22} {"الدرجة":<10} {"التقييم"}')
    print('-' * 52)
    for i, row in results_df.head(top_n).iterrows():
        s = row['final_score']
        if s >= 70:   lbl = 'مناسب جداً'
        elif s >= 50: lbl = 'مناسب'
        else:         lbl = 'غير مناسب'
        print(f'{i+1:<4} {row["neighborhood"]:<22} {s:<10.1f} {lbl}')

    return results_df

