print(confusion_matrix(y_clean, y_pred))

"""## 7. Train Final Model with Probability Calibration"""

best_model.fit(X_clean, y_clean)

# Calibrate probabilities so predict_proba values are more reliable
calibrated_model = CalibratedClassifierCV(best_model, cv=5, method='isotonic')
