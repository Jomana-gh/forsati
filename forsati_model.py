import pandas as pd
import numpy as np
from xgboost import XGBClassifier
from difflib import get_close_matches

from sklearn.model_selection import (
    GroupKFold, cross_val_score, cross_val_predict, RandomizedSearchCV
)
from sklearn.preprocessing import LabelEncoder
from sklearn.calibration import CalibratedClassifierCV
from sklearn.metrics import classification_report, confusion_matrix
from sklearn.inspection import permutation_importance

import warnings
warnings.filterwarnings('ignore')

"""## 1. Load Data"""

df = pd.read_csv('forsati_final_dataset.csv')
print(f'Shape: {df.shape}')
print(f'NaN in success column: {df["success"].isna().sum()}')
df.head()

"""## 2. Encoding"""

# Category label encoding
le = LabelEncoder()
df['category_encoded'] = le.fit_transform(df['category_en'])

# Region one-hot encoding
region_map = {0: 'Central', 1: 'East', 2: 'North', 3: 'South', 4: 'West'}
df['region'] = df['region_encoded'].map(region_map)
region_dummies = pd.get_dummies(df['region'], prefix='region')
df = pd.concat([df, region_dummies], axis=1)

# Target encoding for neighborhood (mean success rate per neighborhood)
neighborhood_success_rate = df.groupby('neighborhood')['success'].mean()
df['neighborhood_success_rate'] = df['neighborhood'].map(neighborhood_success_rate)
df['neighborhood_success_rate'] = df['neighborhood_success_rate'].fillna(df['success'].mean())

# Target encoding for category
category_success_rate = df.groupby('category_en')['success'].mean()
df['category_success_rate'] = df['category_en'].map(category_success_rate)
df['category_success_rate'] = df['category_success_rate'].fillna(df['success'].mean())

print('Encoding done.')

"""## 3. Feature Engineering"""

# Market saturation: share of this category among all businesses
df['category_share'] = df['category_count'] / (df['total_businesses'] + 1)

# Demand-supply: population per business
df['pop_per_business'] = df['Population'] / (df['total_businesses'] + 1)

# Transport accessibility index
df['transport_index'] = df['metro'] + df['bus'] + df['public_station']

# Nearby competition density
df['nearby_competition_density'] = df['nearby_same_category'] / (df['nearby_all_businesses'] + 1)

# Economic activity proxy
df['economic_index'] = df['annual_transactions'] / (df['Population'] + 1)

# Population density per km2
df['pop_density_per_km2'] = df['Population'] / (df['Area_km2'] + 0.01)

print('Feature engineering done.')

"""## 4. Define Features & Clean Data"""

feature_cols = [
    # Original features
    'category_encoded',
    'category_count',
    'total_businesses',
    'competition_ratio',
    'nearby_same_category',
    'nearby_all_businesses',
    'population_density',
    'avg_price_m2',
    'business_licenses',
    'street_count',
    'annual_transactions',
    'Population',
    'Area_km2',
    'metro',
    'bus',
    'public_station',

    # Target-encoded features
    'neighborhood_success_rate',
    'category_success_rate',

    # Engineered features
    'category_share',
    'pop_per_business',
    'transport_index',
    'nearby_competition_density',
    'economic_index',
    'pop_density_per_km2',
]

# Add one-hot region columns
feature_cols += list(region_dummies.columns)

X = df[feature_cols]
y = df['success']

# --- Drop rows where target (y) is NaN ---
valid_mask   = y.notna()
X_clean      = X[valid_mask].reset_index(drop=True)
y_clean      = y[valid_mask].astype(int).reset_index(drop=True)
groups_clean = df.loc[valid_mask, 'neighborhood'].reset_index(drop=True)

print(f'Total rows: {len(df)}')
print(f'Dropped {(~valid_mask).sum()} rows with NaN in success column.')
print(f'Remaining rows: {len(y_clean)}')
print(f'Class balance — 0: {(y_clean==0).sum()}, 1: {(y_clean==1).sum()}')

"""## 5. Hyperparameter Tuning"""

# Automatic class imbalance handling
neg_count = (y_clean == 0).sum()
pos_count = (y_clean == 1).sum()
spw = neg_count / pos_count if pos_count > 0 else 1
print(f'scale_pos_weight = {spw:.2f}')

param_dist = {
    'n_estimators':     [100, 200, 300, 500],
    'max_depth':        [3, 4, 5, 6, 7],
    'learning_rate':    [0.01, 0.05, 0.1, 0.15],
    'subsample':        [0.6, 0.7, 0.8, 0.9, 1.0],
    'colsample_bytree': [0.5, 0.6, 0.7, 0.8, 1.0],
    'min_child_weight': [1, 3, 5, 7],
    'gamma':            [0, 0.1, 0.2, 0.5],
    'reg_alpha':        [0, 0.01, 0.1, 1],
    'reg_lambda':       [1, 1.5, 2, 5],
}

base_xgb = XGBClassifier(
    random_state=42,
    eval_metric='logloss',
    scale_pos_weight=spw,
    tree_method='hist',
)

# GroupKFold: no shuffle or random_state arguments
cv_group = GroupKFold(n_splits=5)

search = RandomizedSearchCV(
    base_xgb,
    param_distributions=param_dist,
    n_iter=30,
    scoring='f1',
    cv=cv_group,
    random_state=42,
    n_jobs=-1,
    verbose=1
)

search.fit(X_clean, y_clean, groups=groups_clean)
print(f'\nBest F1 score: {search.best_score_:.4f}')
print(f'Best params: {search.best_params_}')

"""## 6. Cross-Validation Evaluation"""

best_model = search.best_estimator_

for metric in ['accuracy', 'f1', 'roc_auc']:
    scores = cross_val_score(
        best_model, X_clean, y_clean,
        cv=cv_group, groups=groups_clean, scoring=metric
    )
    print(f'{metric:10s}: {scores.mean()*100:.2f}% ± {scores.std()*100:.2f}%')

y_pred = cross_val_predict(
    best_model, X_clean, y_clean,
    cv=cv_group, groups=groups_clean
)

print('\n=== Classification Report ===')
print(classification_report(y_clean, y_pred, target_names=['غير ناجح', 'ناجح']))
